package com.example.cse226ca1;

public class P01Item {
    String name;
    String reg;
    int image;

    public P01Item(String name, String reg, int image) {
        this.name = name;
        this.reg = reg;
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public String getReg() {
        return reg;
    }

    public int getImage() {
        return image;
    }
}

